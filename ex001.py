a = input('Digite alguma coisa: ')

print(f'O tipo de digitado nesse input é {type(a)}')
print(f'Só tem espaços ? {a.isspace()}')
print(f'é um número? {a.isnumeric()}')
print(f'é alfabetico? {a.isalpha()}')
print(f'É alfanumérico? {a.isanum()}')
print(f'éstá minúcusla? {a.islower()}')
print(f' está maiúsculo? {a.isupper()}')
print(f'está capitalizada? {a.istitle()}')




